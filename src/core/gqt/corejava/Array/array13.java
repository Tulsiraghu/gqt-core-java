package core.gqt.corejava.Array;

public class array13 {
	 public static void main(String[] args) {
	        int[] arr = {1, 2, 3, 4};
	        int product = 1;
	        for (int i : arr) {
	            product *= i;
	        }
	        System.out.println("Product of array elements: " + product);
	    }

}
